//Classe
public class Do_while{

    //Método principal da classe
    public static void main(String[] args){

        //Declaração das variáveis
        int i = 0;

        //Estrtura de repetição
        do{
            System.out.println(i);
            i++;
        } 
        while (i <= 10);

 }//Fim do método
}//Fim da classe